# Logical Operators Example
num1 = 10
num2 = 5
print("Logical Operators Example")
logical_and_result = num1 > 0 and num2 < 10
print("Logical AND:", logical_and_result)
logical_or_result = num1 < 0 or num2 > 10
print("Logical OR:", logical_or_result)
logical_not_num1 = not num1 > 0
logical_not_num2 = not num2 < 10
print("Logical NOT (num1):", logical_not_num1)
print("Logical NOT (num2):", logical_not_num2)

# Identity Operators Example
p = [1, 2, 3]
q = p
r = [1, 2, 3]
print("Identity Operators Example")
result_1 = p is q
result_2 = p is r
print("p is q:", result_1)
print("p is r:", result_2)
not_result_1 = p is not q
not_result_2 = p is not r
print("p is not q:", not_result_1)
print("p is not r:", not_result_2)

# Membership Operators Example
my_list = [1, 2, 3, 4, 5]
print("Membership Operators Example")
in_result_1 = 3 in my_list
in_result_2 = 6 in my_list
print("3 in my list:", in_result_1)
print("6 in my list:", in_result_2)
not_in_result_1 = 3 not in my_list
not_in_result_2 = 6 not in my_list
print("3 not in my list:", not_in_result_1)
print("6 not in my list:", not_in_result_2)

